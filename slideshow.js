const slideshowContainer = document.getElementById('slideshow');
const tooltip = document.getElementById('tooltip');
let currentSlide = 0;

slidesData.forEach((item) => {
  const slide = document.createElement('div');
  slide.className = 'slide';

  if (item.imageUrl) {
    const img = document.createElement('img');
    img.src = item.imageUrl;
    img.alt = 'Slide Image';

    img.addEventListener('click', (e) => {
      if (item.imageSources) {
        showTooltip(e.clientX, e.clientY, item.imageSources);
      }
    });

    slide.appendChild(img);
  }

  if (item.description) {
    const textDiv = document.createElement('div');
    textDiv.className = 'description';
  
    if (item.type === 'worksCited' || item.type === 'imageCitations') {
      textDiv.classList.add('small-text');
    }
  
    if (item.title) {
      const titleElement = document.createElement('h2');
      titleElement.className = 'slide-title';
      titleElement.textContent = item.title;
      textDiv.appendChild(titleElement);
    }
  
    const textContent = document.createElement('div');
    textContent.className = 'description-content';
    textContent.innerHTML = item.description;
  
    textDiv.appendChild(textContent);
    slide.appendChild(textDiv);
  }
  

  if (!item.imageUrl) {
    slide.classList.add('text-only');
  }
  

  slideshowContainer.appendChild(slide);
});


const slides = document.querySelectorAll('.slide');

showSlide(currentSlide);

function showSlide(index) {
  slides.forEach((slide, i) => {
    slide.classList.remove('show');
  });
  slides[index].classList.add('show');
}

function nextSlide() {
  currentSlide = (currentSlide + 1) % slides.length;
  hideTooltip();
  showSlide(currentSlide);
}

function prevSlide() {
  currentSlide = (currentSlide - 1 + slides.length) % slides.length;
  hideTooltip();
  showSlide(currentSlide);
}

function showTooltip(x, y, text) {
  tooltip.textContent = text;
  tooltip.style.left = `${x + 10}px`; 
  tooltip.style.top = `${y + 10}px`;
  tooltip.style.opacity = 1;
}

function hideTooltip() {
  tooltip.style.opacity = 0;
}

document.addEventListener('click', (e) => {
  if (!e.target.matches('img')) {
    hideTooltip();
  }
});